
public class tag {
	
	int id;
	double x,y,z;
	String name;
	public double width = 0, height = 0;
	public double fontSize;
	
	public int getID() {
		return id;
	}
	public void setID(int id) {
		this.id = id;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public double getZ() {
		return z;
	}
	public void setZ(double z) {
		this.z = z;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}	

}
